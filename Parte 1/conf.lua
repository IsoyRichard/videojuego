
function love.conf(t)
    t.title = "Platformer"                -- Titulo de juego (string)
    t.version = "11.3"                    -- Love Version usado (string)
    t.console = true                      --  (boolean, Windows only)
    t.window.width = 1280                 -- Windows Ancho Tamaño (number)
    t.window.height = 720                 -- Windows Alto Tamaño (number)
    t.window.vsync = 0
end
